function AdminLogout() {
  const router = useRouter();
  async function handleLogout() {
    await fetch("/api/admin/auth/logout", { method: "POST" });
    router.push("/admin/login");
  }
  return (
    <button
      onClick={handleLogout}
      className="flex items-center gap-2.5 w-full px-3 py-2.5 rounded-xl text-sm font-medium text-gray-500 hover:bg-gray-50 hover:text-gray-900 transition-colors"
    >
      <svg className="w-4 h-4 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.8} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
      </svg>
      Sign Out
    </button>
  );
}

export function AdminSidebar() {
  const pathname = usePathname();

  const HIDE_SIDEBAR_ON = ["/admin/login", "/admin/verify-2fa", "/admin/setup-2fa"];

  if (HIDE_SIDEBAR_ON.includes(pathname)) {
    return null;
  }
  return (
    <AppSidebar
      groups={groups}
      exactMatch={["/admin/dashboard"]}
      bottomSlot={<AdminLogout />}
    />
  );
}
